package com.spm.sejarah;

public class listKelas {

    public String kelasId;
    public String teachIc;
    public String kelasName;

    public listKelas() {
    }

    public String getKelasId() {
        return kelasId;
    }

    public void setKelasId(String kelasId) {
        this.kelasId = kelasId;
    }

    public String getTeachIc() {
        return teachIc;
    }

    public void setTeachIc(String teachIc) {
        this.teachIc = teachIc;
    }

    public String getKelasName() {
        return kelasName;
    }

    public void setKelasName(String kelasName) {
        this.kelasName = kelasName;
    }
}
